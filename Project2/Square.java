public class Square
{
    /**
     * Returns area of a square with the given side.
     * (Postcondition: area > 0)
     * @param side the length of the side of the square
     * @return area of the square
     * (Precondition: side > 0)
     */
    
    public static double getArea(double side)
    {
        return side * side; 
    }
}

       